    t = t.replace('[=攻擊範圍]','[=攻擊區]') # アタックエリア → 攻擊範圍（AI 誤）
    t = t.replace('[=拋球]',    '[=舉球]')   # トス → 拋球（AI 誤）
    t = t.replace('[=投擲]',    '[=舉球]')   # トス → 投擲（AI 誤）
