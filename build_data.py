    t = re.sub(r'\[=一次觸摸\((\w+)\)\]', r'[=一觸(\1)]', t)
    # [=一擊(N)]（AI 誤譯 ワンタッチ，僅在 [=...] 標記內）→ [=一觸(N)]
    t = re.sub(r'\[=一擊\((\w+)\)\]', r'[=一觸(\1)]', t)
    # [=兩次攻擊(N)]（AI 誤譯 ツーアタック）→ [=二段攻擊(N)]
    t = re.sub(r'\[=兩次攻擊\((\w+)\)\]', r'[=二段攻擊(\1)]', t)
